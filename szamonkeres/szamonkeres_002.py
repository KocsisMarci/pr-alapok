"""
Bekérek egy egész számot, irassuk ki, hogy a szám pozitív negatív vagy netán 0!
"""

szam=int(input("Írj be egy számot: "))
if szam>0:
    print("Az általad beírt szám pozitív")
if szam<0:
    print("Az általad beírt szám negatív")
if szam==0:
    print("Az általad beírt szám 0")